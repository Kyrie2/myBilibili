		$videoItem.each(function(index) {
			//simulationData.animationItemInfos[newestFlag]
			var imgNum = data[index].bgImgLen;
			var averageLen = 160 / imgNum;
			$(this).hover(function(ev) {
				var _this = this;
				var posX = ev.pageX - $(this).offset().left;
				var currentImgNum = Math.round(posX / averageLen);
				var currentPosX = -160 * (currentImgNum % 10);
				var currentPosY = -90 * (Math.ceil(currentImgNum / 10));
				timer = setTimeout(function() {
					$(".progress_box", _this).show();
					$(".video_item_video", _this).addClass("noBg");
					$(".progress", this).css("width", posX);
					$(".video_item_bg", _this).removeClass("noBg").css("backgroundPosition", currentPosX + "px" + currentPosY + "px");
				}, 800);
			}, function() {
				$(".video_item_bg", this).addClass("noBg");
				$(".video_item_video", this).removeClass("noBg");
				$(".progress_box", this).hide();
				clearTimeout(timer);
			}).mousemove(function(ev) {
				var posX = ev.pageX - $(this).offset().left;
				$(".progress", this).css("width", posX);
				var currentImgNum = Math.round(posX / averageLen);
				var currentPosX = -160 * (currentImgNum % 10);
				var currentPosY = -90 * (Math.ceil(currentImgNum / 10) - 1);
				$(".video_item_bg", this).css({
					"background-positionX": currentPosX,
					"background-positionY": currentPosY
				});
			});
		});

